#include <unistd.h>

int		ft_str_is_alpha(char *str)
{
	int n;

	n = 0;
	if (str[0] == '\0')
		return (1);
	while (str[n] != '\0')
	{
		if ((str[n] <= 'z' && str[n] >= 'a')
				|| (str[n] >= 'A' && str[n] <= 'Z'))
			n++;
		else
			return (0);
	}
	return (1);
}
